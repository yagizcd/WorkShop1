public class workShop1Task2 {
    public static void main(String args) {
        //2- Do-While ve While döngüsü nedir? Bununla ilgili örnek yapınız.
        int j=1;
        int k=1;
        while(j<1){
            System.out.print(j+" ");
            j++;
        }
        System.out.println("");
        System.out.println("While dongusu bitti");
        do {
            System.out.print(k+" ");
            k++;
        }while(k<1);
    }
}
